print('==== Operadores Aritméticos ====')

# Adição
print(5+3)

# Subtração
print(5-3)

# Multiplicação
print(5*3)

# Divisão
print(5/3)

# Potência ou Exponênciação (Normal ou c/ Pow)
print(5**3)
print(pow(5,3))

# Divisão Inteira
print(5//3)

# Resto da Divisão ou Módulo
print(5 % 3)

# Raiz Quadrada
print(81**(1/2))

# Raiz Cúbica
print(127**(1/3))